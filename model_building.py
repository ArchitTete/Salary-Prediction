import pandas as pd
from sklearn.preprocessing import LabelEncoder
import matplotlib.pyplot as plt
import seaborn as sns
from sklearn.model_selection import train_test_split
from sklearn.linear_model import LinearRegression
from sklearn.metrics import mean_squared_error, r2_score
from sklearn.ensemble import RandomForestRegressor
from sklearn.tree import DecisionTreeRegressor
from sklearn.svm import SVR
from sklearn.neighbors import KNeighborsRegressor

df =pd.read_csv("/content/Salary_Data.csv")

df

df.isna()

df.info()

df.isnull().sum()

for col in ['Age', 'Years of Experience', 'Salary']:
    if df[col].isnull().any():
        df[col] = df[col].fillna(df[col].mean())

for col in ['Gender', 'Education Level', 'Job Title']:
    if df[col].isnull().any():
        df[col] = df[col].fillna(df[col].mode()[0])

display(df.isnull().sum())

label_encoder = LabelEncoder()
for col in ['Gender', 'Education Level', 'Job Title']:
    df[col] = label_encoder.fit_transform(df[col])

display(df.head())

x=df.drop('Salary',axis=1)
y=df['Salary']

x_train,x_test,y_train,y_test =train_test_split(x,y,test_size=.2,random_state=42)
# Initialize models
linear_reg_model = LinearRegression()
svr_model = SVR()
knn_model = KNeighborsRegressor()
decision_tree_model = DecisionTreeRegressor()
random_forest_model = RandomForestRegressor()

# Train Linear Regression model
linear_reg_model.fit(x_train, y_train)

# Make predictions
y_pred_lr = linear_reg_model.predict(x_test)

# Evaluate the model
mse_lr = mean_squared_error(y_test, y_pred_lr)
r2_lr = r2_score(y_test, y_pred_lr)

print(f"Linear Regression - Mean Squared Error: {mse_lr:.2f}")
print(f"Linear Regression - R-squared: {r2_lr:.2f}")

# Train SVR model
svr_model.fit(x_train, y_train)

# Make predictions
y_pred_svr = svr_model.predict(x_test)

# Evaluate the model
mse_svr = mean_squared_error(y_test, y_pred_svr)
r2_svr = r2_score(y_test, y_pred_svr)

print(f"SVR - Mean Squared Error: {mse_svr:.2f}")
print(f"SVR - R-squared: {r2_svr:.2f}")

# Train Random Forest Regressor model
random_forest_model.fit(x_train, y_train)

# Make predictions
y_pred_rf = random_forest_model.predict(x_test)

# Evaluate the model
mse_rf = mean_squared_error(y_test, y_pred_rf)
r2_rf = r2_score(y_test, y_pred_rf)

print(f"Random Forest Regressor - Mean Squared Error: {mse_rf:.2f}")
print(f"Random Forest Regressor - R-squared: {r2_rf:.2f}")

# Train Decision Tree Regressor model
decision_tree_model.fit(x_train, y_train)

# Make predictions
y_pred_dt = decision_tree_model.predict(x_test)

# Evaluate the model
mse_dt = mean_squared_error(y_test, y_pred_dt)
r2_dt = r2_score(y_test, y_pred_dt)

print(f"Decision Tree Regressor - Mean Squared Error: {mse_dt:.2f}")
print(f"Decision Tree Regressor - R-squared: {r2_dt:.2f}")

# Train KNN model
knn_model.fit(x_train, y_train)

# Make predictions
y_pred_knn = knn_model.predict(x_test)

# Evaluate the model
mse_knn = mean_squared_error(y_test, y_pred_knn)
r2_knn = r2_score(y_test, y_pred_knn)

print(f"KNN - Mean Squared Error: {mse_knn:.2f}")
print(f"KNN - R-squared: {r2_knn:.2f}")

models = ['Linear Regression', 'SVR', 'Random Forest', 'Decision Tree', 'KNN']
r2_scores = [r2_lr, r2_svr, r2_rf, r2_dt, r2_knn]

plt.figure(figsize=(10, 6))
sns.barplot(x=models, y=r2_scores, hue=models, palette='viridis', legend=False)
plt.title('Comparison of R-squared Scores Across Different Models')
plt.xlabel('Model')
plt.ylabel('R-squared Score')
plt.ylim(0, 1) # R-squared typically ranges from 0 to 1
plt.grid(axis='y', linestyle='--', alpha=0.7)
plt.xticks(rotation=45, ha='right')
plt.tight_layout()
plt.show()

print("Linear Regression Model Coefficients:")
for feature, coef in zip(x.columns, linear_reg_model.coef_):
    print(f"{feature}: {coef:.2f}")
print(f"Intercept: {linear_reg_model.intercept_:.2f}")

