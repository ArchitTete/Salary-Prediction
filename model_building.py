years_slider = widgets.IntSlider(
    value=int(x_train['Years of Experience'].mean()),
    min=int(x_train['Years of Experience'].min()),
    max=int(x_train['Years of Experience'].max()),
    step=1,
    description='Years of Experience:',
    continuous_update=True,
    orientation='horizontal',
    readout=True,
    readout_format='d'
)

